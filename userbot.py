@app.on_message(filters.private & filters.incoming & filters.text)
async def auto_responder(client, message):
    # Не отвечаем сами себе
    if message.from_user.id == client.me.id:
        return
    
    text_lower = message.text.lower()
    print(f"📩 Автоответ от {message.from_user.first_name}: {message.text[:50]}...")
    
    # Ищем совпадения
    for keywords, reply_text in AUTO_RESPONSES.items():
        if any(keyword in text_lower for keyword in keywords):
            print(f"✅ Найдено ключевое слово: {keywords[0]}")
            try:
                await asyncio.sleep(1)
                await message.reply(reply_text)
                print(f"✅ Отправлен ответ: {reply_text[:30]}...")
                break
            except FloodWait as e:
                print(f"⏳ Флуд: ждем {e.value} сек")
                await asyncio.sleep(e.value)
            except Exception as e:
                print(f"❌ Ошибка автоответа: {e}")

